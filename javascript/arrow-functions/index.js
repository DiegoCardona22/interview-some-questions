// Arrow functions son una forma más corta de escribir funciones en JavaScript.
// En este ejemplo, se muestra la sintaxis de una función normal y la sintaxis de una función de flecha.
console.log(sum1(1, 2));

// Función normal
function sum1(a, b) {
  return a + b;
}

// Función de flecha
const sum2 = (a, b) => a + b;
